#include <stddef.h>
#include <string.h>

/********************************************************************************************************************/

const void* memchr(const void* ptr, int value, size_t sz)
{
    const char* p = (const char*)ptr;

    for (size_t i = 0; i < sz; ++i)
    {
        if (p[i] == (char)value)
            return (p + i);
    }

    return NULL;
}

