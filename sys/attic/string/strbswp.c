#include "cdefs.h"
#include "stdlib.h"
#include "string.h"

/*************************************************************************/
/*
 * Performs a byte swap on the string.
 */
char *strbswp(char *str, size_t sz)
{
    size_t i;
    char c;

    if (sz & 1)
	--sz;

    for (i = 0; i < sz; i += 2)
    {
	c = str[i];
	str[i] = str[i + 1];
	str[i + 1] = c;
    }

    return str;
}

