#include <stdio.h>

/********************************************************************************************************************/

int snprintf(char* sbuf, size_t slen, const char* fmt, ...)
{
    int rval = 0;
    va_list args;

    va_start(args, fmt);
    rval = vsnprintf(sbuf, slen, fmt, args);
    va_end(args);

    return rval;
}

